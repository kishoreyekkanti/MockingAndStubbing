package models;

public class Feed {
    private String url;
    private String commaSepratedTags;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getCommaSepratedTags() {
        return commaSepratedTags;
    }

    public void setCommaSepratedTags(String commaSepratedTags) {
        this.commaSepratedTags = commaSepratedTags;
    }
}
